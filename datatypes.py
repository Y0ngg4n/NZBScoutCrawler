class Movie:

    def __init__(self, url, nzb_url, title, description, length, language, posted, category):
        self.url = url
        self.nzb_url = nzb_url
        self.title = title
        self.description = description
        self.length = length
        self.language = language
        self.posted = posted
        self.category = category

